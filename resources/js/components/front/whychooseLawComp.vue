However, it is always advisable that you paste the syllabus on the places of your sight, so that whenever you will move around that place, in some way or the other you will end up reading the syllabus time and again, which will further lead to memorization of all the significant portions of the syllabus. This will not let you lose your focus and you will always stay in the broad contours of the areas of the syllabus created by you in your mind. We advise you to strictly adhere to the syllabus as furnished by UPSC from time to time. 
 
