                                    <h4>
                                        PREPARATION STRATEGY FOR ASPIRANTS HAVING A LAW BACKGROUND 
                                    </h4>
                                    <p>
                                        Aspirants having a law background have already covered numerous laws during their academics and thus they are already versed with the academic part of law. However, as we have already discussed that the strategy required for the preparation of law optional is different from our academics, the aspirants who have already studied law in their college life are now expected to apply such knowledge in accordance with the need of the UPSC exam. The pattern of questions asked in law optional subject tilts towards contemporary legal issues and therefore it is expected from the aspirant to apply their legal knowledge in a restrictive manner keeping in mind the requirement of the exam. 
                                    </p>
                                    <p>
                                        In UPSC Law Optional Examination, the study of various legal provisions, case laws, authorities, judgments, etc. is done in a different way than that of the college examinations, but cramming all the Laws in both the scenarios is neither advisable nor feasible. Also, in college, more focus is generally on filling up the sheets; whereas, in Law Optional, your emphasis must be on the precise and crisp writing of the answer within the prescribed word limit. Therefore, in order to start your preparation journey, you need to keep the following points in mind: 
                                        <br>
                                        <ol class="ml-5" type="I">
                                            <li>
                                                <strong>
                                                    Peruse the syllabus of the Law Optional furnished by the Union Public Service Commission:
                                                </strong>
                                                <br>
                                                Detailed analysis of each and every heading and sub-heading of the syllabus of Law Optional is the first step towards the beginning of the preparation phase. It is always advisable that you paste the syllabus on the places of your sight, so that whenever you will move around that place, in some way or the other you will end up reading the syllabus time and again, which will further lead to stimulation of your mind. This will not let you lose your focus and you will always stay in the broad contours of the areas of the syllabus created by you, in your memory. 
                                            </li>
                                            <li>
                                                <strong>
                                                    Analysis of the college syllabi and Law Optional together:
                                                </strong>
                                                <br>
                                               It's time to go back to the basics. You need to analyze the syllabus of your college as well as the syllabus of your Law Optional together. After analyzing both the syllabi, you will find that the maximum portion, which formulates the syllabus of Law Optional was taught to you, during your Law Degree. There is an overlapping of almost 70 to 80 percent of the syllabus. So, if you have studied diligently during your LL.B. course with due attention, this means that you have crossed the first milestone. After having the basic knowledge of the subjects to be studied in the Law Optional, now all you need is to devise a plan in accordance with the UPSC Examination Pattern. It is pertinent to note that even if there is an overlap in the syllabus, the approach toward answering the questions is way apart. Unlike college exams, UPSC Examination requires crisp and precise answers which follow the word limit as prescribed against each question. Therefore, here instead of mugging up and jotting down whatever you remember is not the requirement. Instead, you must create a mental synopsis of your answer and then write it within the word limit provided. 
                                            </li>
