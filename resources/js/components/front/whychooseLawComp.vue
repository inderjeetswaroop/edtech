                                        PREPARATION STRATEGY FOR ASPIRANTS WITHOUT A LAW BACKGROUND 
                                    </h4>
                                    <p>
                                       At the outset, we must make it clear that having a law degree is not at all necessary for someone to ace law optional subject, after all, law is a very practical subject that relates to everyday happenings. Any aspirant who is preparing for their General Studies Paper is already well versed with contemporary issues and therefore while preparing for law optional there won't be any need of reading something special. 
                                    </p>
                                    <p>
                                        Most of the topics under Law Optional are already covered under GS Paper due to the huge overlapping in them and thus even if the aspirant is not from a legal background, he will be able to understand and grasp the concepts very easily. As we have discussed earlier, the Indian Polity portion of General Studies Paper-II comprises the Constitutional Law. It is overlapping with a significant portion of Law Optional as well. Therefore, commencing the preparation for Law Optional with the Supreme Law of the land i.e., Constitutional Law becomes imperative. This will help you big time in the requisite understanding of the significant basic legal terms and building a premise for a better understanding of other Laws such as Administrative Laws, Law of Crimes, Law of Contract, Environmental Law, Law of Competition etc.
                                        <br>
                                        It is a myth among aspirants that law optional is very tough and if you are not from a law background, you won’t be able to comprehend it. This is not true at all, and there are many people who aced at law optional without having any law degree, the biggest example being Jasbir Singh Bajaj Sir, who because of his interest and enthusiasm in law opted for law as his optional and ended up becoming an IAS Officer. 
                                        <br>
                                        If we talk about the strategy of approaching the Law Optional for the non-law graduates, it is somewhat different to a certain extent. When an aspirant has an interest in law, a lack of a LL.B. Degree won’t become a hindrance for him and therefore Jasbir Singh Bajaj Sir has recommended a different strategy for approaching the examination for those lacking a law degree.
                                        <br>
                                        According to him, in order to approach the examination, an aspirant must take into consideration the following points:
                                        <br>
                                        <ol class="ml-5" type="I">
                                            <li>
                                                <strong>
                                                    Detailed perusal of the past year’s question papers:
                                                </strong>
                                                <br>
                                                After the basic understanding of the laws, it becomes imperative to understand what type of questions are asked by the Union Public Service Commission in the examination. A deep perusal of the past year’s question papers becomes imperative at this juncture. The aspirants are hereby required to make a separate copy or a register and section it accordingly in different heads of the laws which are asked as per the syllabus, furnished by the UPSC. The concepts derived from the questions asked in the past years are required to be incorporated into the register along with the number of times they have been repeated.  
                                            </li>
                                            <li>
                                                <strong>
                                                    Going through the syllabus of Law Optional:
                                                </strong>
                                                <br>
