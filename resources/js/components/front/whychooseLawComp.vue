                                                <strong>
                                                    Prudent analysis of Past Year Question Papers:
                                                </strong>
                                                <br>
                                                In order to commence your preparation, start a prudent analysis of the Past Year Question Papers and make a separate register or notebook to jot down the list of frequently asked questions as well as the concepts.
                                            </li>
                                            <li>
                                                <strong>
                                                    Move ahead with your UPSC Civil Services journey with Law Optional:
